public class LoopNP {
    public static void main(String[] args){
        // Loop de números binários até 100
        for(int num = 2; num <= 100; num++){
            if(num%2==0 && num!=2 || num%3==0 && num!=3 || num%5==0 || num==89 || num==91 || num==49);
            else
                System.out.println(num);
        }
    }
}
